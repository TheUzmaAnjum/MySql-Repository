-- Using Temporary Tables
-- Temporary tables are tables that are only visible to the session that they are created in. 
-- They can be used to store intermediate results for complex queries or to manipulate data before inserting it into a permanent table.

-- There's 2 ways to create temp tables:
-- 1. This is the less commonly used way - which is to build it exactly like a real table and insert data into it
 CREATE TEMPORARY TABLE temp_table
(
 first_name varchar(50),
 last_name varchar(50),
 favorite_movie varchar(100)
);

-- The table gets created but if we refresh our tables it isn't there. It isn't an actual table. It's just a table in memory.
-- Obviously as it's balnk, we would need to insert data into it like this:
INSERT INTO temp_table
VALUES ('Alexander','James','Lord of the Rings: The Twin Towers');

-- now when we run it and execute it again we have our data
SELECT *
FROM temp_table;

